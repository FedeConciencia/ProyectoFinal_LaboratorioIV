    //METODO BUSCAR ONE RUBRO:
    public RubroArticulo buscarOneRubroArticulo(Long id) {

        Connection conexion = null;
        Conexion con = new Conexion();
        RubroArticulo rubro = null;
        PreparedStatement ps = null;  //Este objeto permite guardar las consultas que hacemos a la BD.
        ResultSet rs = null;  // este objeto lo usamos cuando obtenemos algo de la base de datos.

        try {

            conexion = con.getConnection(); //metodo getConnection, logueamos el usuario.

            ps = conexion.prepareStatement("SELECT *  FROM rubro_articulo WHERE idRubro = ?");

            ps.setLong(1, id); //pasamos el id parametro y se ingresa en el ? del query

            rs = ps.executeQuery();  //Ejecutamos el Resulset y executeQuery cuando obtenemos algo de la base de datos.

            if (rs.next()) {  //si nos devuelve un dato true

                Long idRubro = rs.getLong(1); //cada numero del parametro hace referencia al dato del campo que se desea obtener = idPersona
                String denominacion = rs.getString(2);
                LocalDate fechaAlta = (rs.getDate(3)).toLocalDate();
                LocalDate fechaBaja = (rs.getDate(4)).toLocalDate();
                String estado = rs.getString(5);

                rubro = new RubroArticulo(idRubro, denominacion, fechaAlta, fechaBaja, estado);

                System.out.println("El Registro fue encontrado con exito.");
                //JOptionPane.showMessageDialog(null, "El Registro fue encontrado con exito.");

            } else {

                System.out.println("El Registro no fue encontrado en la Base de Datos.");
                //JOptionPane.showMessageDialog(null, "El Registro no fue encontrado en la Base de Datos.");
            }

            conexion.close();

        } catch (Exception ex) {

            System.err.println("Error. " + ex);

        } finally {

            try {

                ps.close();
                rs.close();

            } catch (SQLException ex) {
                System.err.println("Error. " + ex);
            }

        }

        return rubro; //devolvemos el alumno encontrado
    }
    
    //OBTENER ALL RUBRO:
    public List<RubroArticulo> buscarAllRubroArticulo() {

        Connection conexion = null;
        Conexion con = new Conexion();
        RubroArticulo rubro = null;
        List<RubroArticulo> listaRubro = new ArrayList<RubroArticulo>();
        PreparedStatement ps = null;  //Este objeto permite guardar las consultas que hacemos a la BD.
        ResultSet rs = null;  // este objeto lo usamos cuando obtenemos algo de la base de datos.

        try {

            conexion = con.getConnection(); //metodo getConnection, logueamos el usuario.

            ps = conexion.prepareStatement("SELECT * FROM rubro_articulo");

            rs = ps.executeQuery();

            while (rs.next()) {

                Long idRubro = rs.getLong(1); //cada numero del parametro hace referencia al dato del campo que se desea obtener = idPersona
                String denominacion = rs.getString(2);
                LocalDate fechaAlta = (rs.getDate(3)).toLocalDate();
                LocalDate fechaBaja = (rs.getDate(4)).toLocalDate();
                String estado = rs.getString(5);

                rubro = new RubroArticulo(idRubro, denominacion, fechaAlta, fechaBaja, estado);

                listaRubro.add(rubro);

            }

            conexion.close();

        } catch (Exception ex) {

            System.err.println("Error. " + ex);

        } finally {

            try {

                ps.close();
                rs.close();

            } catch (SQLException ex) {
                System.err.println("Error. " + ex);
            }

        }

        return listaRubro; //devolvemos la lista de alumnos encontrado

    }
    
